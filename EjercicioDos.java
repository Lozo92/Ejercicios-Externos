package com.jonathan;

public class EjercicioDos {

    public static void main(String[] args) {

        //Programa que muestre 10 palabras en inglés y su traducción en dos columnas.
        //Se utiliza el tabulador \t para crear columnas.

        System.out.println("House \t casa");
        System.out.println("Car \t coche");
        System.out.println("Red \t rojo");
        System.out.println("Hello \t hola");
        System.out.println("World \t mundo");
        System.out.println("Horse \t caballo");
        System.out.println("Dog \t perro");
        System.out.println("Sea \t mar");
        System.out.println("Earth \t tierra");
        System.out.println("Plane \t avion");


        //Añadimos ahora tabulación de punto y aparte.

        System.out.println("Cat \n gato");
        System.out.println("Name \n nombre");
    }
}
